def ms_to_kmh(m_s):
    """Преобразует м/с в км/ч."""
    return m_s * 3.6

def kmh_to_ms(kmh):
    """Преобразует км/ч в м/с."""
    return kmh / 3.6

def ms_to_mph(m_s):
    """Преобразует м/с в мили/ч."""
    return m_s * 2.23694

def mph_to_ms(mph):
    """Преобразует мили/ч в м/с."""
    return mph / 2.23694
